char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != 0)
	{
		if (str[i - 1] == ' ' || str[i - 1] == '-'
			|| str[i - 1] == '+' || i - 1 == -1)
		{
			if (str[i] >= 97 && str[i] <= 122)
			{
				str[i] += ('A' - 'a');
			}
		}
		else
		{
			if (str[i] >= 65 && str[i] <= 90)
			{
				str[i] += ('a' - 'A');
			}
		}
		i++;
	}
	return (str);
}
