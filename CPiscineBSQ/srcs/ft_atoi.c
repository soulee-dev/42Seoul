int	ft_atoi(char *str)
{
	int	num;

	num = 0;
	while (*str != '\0')
	{
		if (*str >= '0' && *str <= '9')
		{
			num = num * 10 + (*str - '0');
			str++;
		}
		else
			return (num);
	}
	return (num);
}
